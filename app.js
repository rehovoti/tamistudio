function getByPath(obj, path) {
  return path.split(".").reduce((acc, key) => (acc && acc[key] != null ? acc[key] : undefined), obj);
}

function applyTranslations(locale) {
  const dict = translations[locale];
  if (!dict) return;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const value = getByPath(dict, key);
    if (typeof value === "string") {
      el.innerHTML = value;
    }
  });
}

function setLocale(locale) {
  const isEnglish = locale === "en";
  document.documentElement.lang = isEnglish ? "en" : "he";
  document.documentElement.dir = isEnglish ? "ltr" : "rtl";
  document.body.dataset.locale = locale;

  const pill = document.querySelector("[data-lang-pill]");
  if (pill) pill.textContent = isEnglish ? "EN" : "HE";

  applyTranslations(locale);
  localStorage.setItem("tamiStudioLocale", locale);
}

function getInitialLocale() {
  const saved = localStorage.getItem("tamiStudioLocale");
  if (saved === "he" || saved === "en") return saved;

  const prefersEnglish = (navigator.language || "").toLowerCase().startsWith("he");
  return prefersEnglish ? "en" : "he";
}

function closeNav() {
  const nav = document.querySelector("[data-nav]");
  const toggle = document.querySelector("[data-nav-toggle]");
  const backdrop = document.querySelector("[data-nav-backdrop]");

  if (nav) nav.dataset.open = "false";
  if (toggle) toggle.setAttribute("aria-expanded", "false");
  if (backdrop) backdrop.hidden = true;
}

function openNav() {
  const nav = document.querySelector("[data-nav]");
  const toggle = document.querySelector("[data-nav-toggle]");
  const backdrop = document.querySelector("[data-nav-backdrop]");

  if (nav) nav.dataset.open = "true";
  if (toggle) toggle.setAttribute("aria-expanded", "true");
  if (backdrop) backdrop.hidden = false;
}

function initNav() {
  const toggle = document.querySelector("[data-nav-toggle]");
  const nav = document.querySelector("[data-nav]");
  const backdrop = document.querySelector("[data-nav-backdrop]");

  if (!toggle || !nav) return;

  toggle.addEventListener("click", () => {
    const isOpen = nav.dataset.open === "true";
    if (isOpen) closeNav();
    else openNav();
  });

  backdrop?.addEventListener("click", closeNav);

  nav.querySelectorAll("a").forEach((a) => {
    a.addEventListener("click", () => {
      // Only close in mobile (toggle visible)
      const isMobile = window.matchMedia("(max-width: 859px)").matches;
      if (isMobile) closeNav();
    });
  });

  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape") closeNav();
  });
}

function initLangToggle() {
  const btn = document.querySelector("[data-lang-toggle]");
  if (!btn) return;

  btn.addEventListener("click", () => {
    const current = document.body.dataset.locale || "he";
    setLocale(current === "he" ? "en" : "he");
  });
}

function initYear() {
  document.querySelectorAll("[data-year]").forEach((el) => {
    el.textContent = String(new Date().getFullYear());
  });
}

function init() {
  initYear();
  initNav();
  initLangToggle();
  setLocale(getInitialLocale());
  closeNav();
}

document.addEventListener("DOMContentLoaded", init);
